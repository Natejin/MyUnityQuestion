HRESULT GameNode::Init()
{
	//타이머란 일정한 주기마다 정해진 함수를 실행한다.
	//1 : 윈도우 핸들
	//2 :  타이머 번호
	//3 :  타이머주기 1000=1초
	//4 :  주기마다 실행할 함수
	//NULL이면 WM_TIMER가 실행
	SetTimer(m_hWnd, 1, 10, NULL);
	//매니저 초기화
	InputManager->Init();

	return S_OK;
}

void GameNode::Release()
{	//타이머 해제

	//해제를 안하면 종료를 해도 메모리가 줄줄줄~
	KillTimer(m_hWnd, 1);
	//매니저 해제
	InputManager->releaseSingleton();
	RND->releaseSingleton();
}

